"""
CASTING -> Casting in python is therefore done using constructor functions.
"""

# SPECIFIC A VARIABLE TYPE

# INT
x = int(1)
y = int(2.8)
z = int("3")
print(x, y, z)

# FLOAT
x = float(1)
y = float(2.8)
z = float("3")
w = float("4.2")
print(x, y, z, w)

# STR
x = str("s1")
y = str(2)
z = str(3.0)
print(x, y, z)
