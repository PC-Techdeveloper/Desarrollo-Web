print("*** CONVERTIR DE GRADOS CENTIGRADOS A GRADOS FAHRENHEIT ***")

print()
gradosCentigrados = float(
    input("Ingrese la cantidad de grados centigrados a convertir: ")
)

gradosFahrenheit = (9 / 5) * gradosCentigrados + 32

print(f"La cantidad de grados fahrenheit es: {gradosFahrenheit}")
