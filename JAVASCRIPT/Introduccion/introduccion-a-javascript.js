//Declaraciones -> Instrucción compuesta por una o más líneas de código que representan una acción.

let myVariable = 4;

console.log(myVariable);

//Frases de bloqueo -> Instrucciones de flujo de control.
x = 4;
if (x === 2) {
  console.log(x);
}

//Expresiones -> Es una unidad de código que da como resultado un valor.

console.log(2 + 2);

//Operador de agrupación -> ()
console.log(2 + 2 + 4);

//Los tipos de datos también se pueden coercionar explícitamente.

